# Create a list
my_list = [1, 2, 3, 4]

# Add an element to the end of the list
my_list.append(5)

# Print the list after adding the element
print(my_list)  # Output: [1, 2, 3, 4, 5]

# Reverse the list using reverse() method
my_list.reverse()

# Print the reversed list
print("Reversed list:", my_list)

